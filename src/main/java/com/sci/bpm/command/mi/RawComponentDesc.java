package com.sci.bpm.command.mi;

public class RawComponentDesc {

    private String rawMISerialNo;

    private String rawMatMi;

    private String rawMatDesc;

    private String rawMatType;


    public String getRawMISerialNo() {
        return rawMISerialNo;
    }

    public void setRawMISerialNo(String rawMISerialNo) {
        this.rawMISerialNo = rawMISerialNo;
    }

    public String getRawMatMi() {
        return rawMatMi;
    }

    public void setRawMatMi(String rawMatMi) {
        this.rawMatMi = rawMatMi;
    }

    public String getRawMatDesc() {
        return rawMatDesc;
    }

    public void setRawMatDesc(String rawMatDesc) {
        this.rawMatDesc = rawMatDesc;
    }

    public String getRawMatType() {
        return rawMatType;
    }

    public void setRawMatType(String rawMatType) {
        this.rawMatType = rawMatType;
    }
}
